            // 是否可滚动
            var stop = false; 
            // 窗口resize事件
            $(window).resize(function(){
                init();
            });

            // 点击导航
            $(".nav").on("click", "li", function() {
                var page = $(this).index();
                goPage(page);
            })

            // 设置首页高度
            function setHomeHeight() {
                var wh = $(window).height() > 600? $(window).height() : 600;
                $(".main > div").height(wh);
            }
            // 获取当前页码
            function getPage() {
                scrollArr = [$(".home").offset().top,$(".about-me").offset().top,$(".skill").offset().top,$(".works").offset().top,$(".contact").offset().top];
                var wh = $(window).scrollTop();
                var pages = scrollArr.length - 1;
                for(var i = 0, len = scrollArr.length;i < len; i++) {
                    if(i > 0) {                
                        if(wh == 0) {
                            i = len;
                            pages = 0;
                        } else {
                            if(wh < scrollArr[i]) {
                                pages = i - 1;
                                i = len;
                            }
                        }
                    }
                }
                return pages;
            }
            // 跳转到页面
            function goPage(page) {
                if(!stop) {
                    $(".main > div").eq(page).addClass("now").siblings().removeClass("now");
                    stop = true;
                    var len = scrollArr[page];
                    $("html, body").animate({
                        "scrollTop": len+ "px"
                    },300,function() {
                        stop = false;
                    });      
                }
            }
            // 初始化
            function init() {
                setHomeHeight();
                page = getPage();
                // console.log($(window).height())
            }

            // 翻页
            function pageScroll() {
                var self = this;

                $(window).on("mousewheel DOMMouseScroll", function(e) {
                    e.preventDefault();
                    var delta = (e.originalEvent.wheelDelta && (e.originalEvent.wheelDelta > 0 ? 1 : -1)) || (e.originalEvent.detail && (e.originalEvent.detail > 0 ? -1 : 1));
                    if(getPage() + delta < 0) {
                        var page = 0;
                    } else if ((getPage() + delta) >= ($(".main > div").length-1)) {
                        var page = $(".main > div").length-1;
                    } else {   
                        var page = getPage() + delta;
                    }
                    goPage(page);
                });

